                    قدیمی‌ترین نقاشی‌ها در دنیا که متعلق به حدود ۳۲۰۰۰ هزار سال پیش می‌باشد، در گروته شاوه فرانسه قرار دارد. این نقاشی‌ها که با استفاده از افرای قرمز و رنگ‌دانه سیاه، حکاکی و رنگ شده‌اند، تصاویری از اسب‌ها، کرگدن‌ها، شیرها، بوفالوها و ماموت‌ها را نشان می‌دهند. این‌ها نمونه‌هایی از نقاشی در غار هستند که در اغلب نقاط دنیا وجود دارند.
                    
                    بسیاری از آثار شناخته شده و مشهور هنری امروزی، مانند مونالیزا، نقاشی هستند. هرچند اختلاف نظرهای جزئی در مورد کاملاً هنری بودن آثاری که به روش‌های غیر سنتی و با روش‌های غیر از شیوه‌های کلاسیک خلق می‌شوند وجود دارد اما از لحاظ مفهومی، هنرمندانی که از صدا، نور، آتش‌بازی، چاپگرهای جوهرافشان، پیکسل‌های صفحه نمایشگر و پاستل یا مواد دیگر برای خلق اثرشان استفاده می‌کنند، با کسانی که مواد سنتی و رنگ‌های آمیخته با زرده تخم مرغ را بکار می‌برند، یکسان هستند. در نتیجه به اغلب کارهای هنری از این دست نقاشی گفته می‌شود. </p>
