                    نقاشی، نگارگری یا رسم، فرایندی است که طی آن رنگ بر روی یک سطح مانند کاغذ یا بوم ایجاد نقش می‌کند و اثری خلق می‌شود. فردی که این فرایند توسط او انجام می‌گیرد نقاش نام دارد، به‌خصوص زمانی که نقاشی حرفهٔ شخص باشد.
                    
